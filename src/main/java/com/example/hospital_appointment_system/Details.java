package com.example.hospital_appointment_system;

public class Details {
    String patient_name;
    char doc_code;
    int age;
    String email;
    String reason;

    public String getPatient_name() {
        return patient_name;
    }

    public char getDoc_code() {
        return doc_code;
    }

    public int getAge() {
        return age;
    }

    public String getEmail() {
        return email;
    }

    public String getReason() {
        return reason;
    }
}
